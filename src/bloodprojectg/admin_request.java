    private void loadRequestData() {
    String sql = "SELECT * FROM request";
 
    try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/apsit", "root", "23107097");
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery()) {

        DefaultTableModel model = (DefaultTableModel) request_table.getModel();
        model.setRowCount(0); 

        while (rs.next()) {
             String request_id = rs.getString(1);
            String bloodGroup = rs.getString(2);
            String amount = rs.getString(3);
            String mobile = rs.getString(4);
            String address = rs.getString(5);
            String status = rs.getString(6);
            
            // Add the fetched data to the table row
            model.addRow(new Object[]{request_id, bloodGroup, amount, mobile, address, status});
        }
     
    } catch (Exception ex) {
        System.out.println("Error: " + ex.getMessage());
    }
}


