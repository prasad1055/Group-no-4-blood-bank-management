    private void rejectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rejectActionPerformed
                                    
    String sd = id.getText();
    try {
        Class.forName("com.mysql.jdbc.Driver");
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/apsit", "root", "23107097");

        String sqlSelect = "SELECT status FROM `request` WHERE request_id = ?";
        PreparedStatement ptstSelect = conn.prepareStatement(sqlSelect);
        ptstSelect.setString(1, sd);
        ResultSet statusResult = ptstSelect.executeQuery();

        if (statusResult.next()) {
            String currentStatus = statusResult.getString("status");
            if ("Approved".equalsIgnoreCase(currentStatus)) {
                JOptionPane.showMessageDialog(null, "This request has already been approved and cannot be rejected.");
                return; // Exit the method early
            }
        } else {
            JOptionPane.showMessageDialog(null, "Request ID not found.");
            return; // Exit if the request ID doesn't exist
        }

        // If the status is not approved, proceed to reject the request
        String newstatus = "Rejected";
//        String sqlUpdate = "UPDATE `request` SET status = ? WHERE request_id = ?";
//        PreparedStatement ptstUpdate = conn.prepareStatement(sqlUpdate);
//        ptstUpdate.setString(1, newstatus);
//        ptstUpdate.setString(2, sd);
//        ptstUpdate.executeUpdate();
        
        String sqlss = "SELECT * FROM `request` WHERE request_id = ?";
        PreparedStatement ptstss = conn.prepareStatement(sqlss);
        ptstss.setString(1, sd);
        ResultSet rs = ptstss.executeQuery();

        if (rs.next()) {
            // Assuming your approved_requests table has the same structure as request
            String bloodGroup = rs.getString("blood_group");
            String amount = rs.getString("amount");
            String mobile_no = rs.getString("mobile_no");
            String address = rs.getString("address");
            
            // Insert into approved_requests table
            String sqlInsert = "INSERT INTO `rejected_requests` (request_id, blood_group, amount, mobile_no, address, status) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement ptstInsert = conn.prepareStatement(sqlInsert);
            ptstInsert.setString(1, sd);
            ptstInsert.setString(2, bloodGroup);
            ptstInsert.setString(3, amount);
            ptstInsert.setString(4, mobile_no);
            ptstInsert.setString(5, address);
            ptstInsert.setString(6, newstatus);
            ptstInsert.executeUpdate();
        }
            
            
            
 
        String sqldelet= "DELETE FROM `request` WHERE request_id="+sd;
        PreparedStatement ptstdelet= conn.prepareStatement(sqldelet);
        ptstdelet.executeUpdate();
        
        
        
        
        

        JOptionPane.showMessageDialog(null, "Request has been rejected successfully.");
        
        id.setText(""); // Clear the input field
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    } finally {
        loadRequestData(); // Refresh the request data
    }

    }//GEN-LAST:event_rejectActionPerformed

