    private void loadRequestData(String cityFilter) {
        String sql = "SELECT * FROM approved_camp";
        if (!cityFilter.isEmpty()) {
            sql += " WHERE city = ?"; // Add city filter to SQL if provided
        }

        try (Connection conn = java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/apsit", "root", "23107097");
            PreparedStatement pst = conn.prepareStatement(sql)) {

            if (!cityFilter.isEmpty()) {
                pst.setString(1, cityFilter); // Set the city parameter in the query
            }

            java.sql.ResultSet rs = pst.executeQuery();
            DefaultTableModel model = (DefaultTableModel) camp_info.getModel();
            model.setRowCount(0); // Clear the table before adding new data

            while (rs.next()) {
                String name = rs.getString(2);
                String camp_name = rs.getString(3);
                String city = rs.getString(4);
                String date = rs.getString(5);
                String time = rs.getString(6);
                String address = rs.getString(7);
                String contact_no = rs.getString(8);
                String email = rs.getString(9);
                String arrangment = rs.getString(10);

                // Add the fetched data to the table row
                model.addRow(new Object[]{name, camp_name, city, date, time, address, contact_no, email, arrangment});
            }

        } catch (Exception ex) {
            System.out.println("Error: " + ex.getMessage());
        }
    }





